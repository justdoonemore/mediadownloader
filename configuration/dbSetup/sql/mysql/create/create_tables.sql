CREATE TABLE `${MEDIASERVER.SCHEMA}`.`${SERIES.TABLE}` (
  `id` INT PRIMARY KEY AUTO_INCREMENT,
  `name` VARCHAR(100) UNIQUE NOT NULL,
  `season` INT NOT NULL, 
  `episode` INT NOT NULL
  );

CREATE TABLE `${MEDIASERVER.SCHEMA}`.`${SERIES.DOWNLOAD.TABLE}` (
  `id` INT PRIMARY KEY AUTO_INCREMENT,
  `name` VARCHAR(100) NOT NULL,
  `season` INT NOT NULL, 
  `episode` INT NOT NULL,
  `time` TIMESTAMP NOT NULL,
  UNIQUE (`name`, `season`, `episode`)
  );

ALTER TABLE `${MEDIASERVER.SCHEMA}`.`${SERIES.DOWNLOAD.TABLE}` ADD UNIQUE INDEX(`name`, `season`, `episode`);
  
CREATE TABLE `${MEDIASERVER.SCHEMA}`.`${USERS.TABLE}` (
  `id` INT PRIMARY KEY AUTO_INCREMENT,
  `name` VARCHAR(100) UNIQUE NOT NULL,
  `password` VARCHAR(100) NOT NULL, 
  `roles` VARCHAR(100) NOT NULL,
  `emailaddress` VARCHAR(100) NOT NULL
  );
  
CREATE TABLE `${MEDIASERVER.SCHEMA}`.`${SERIES.NOTIFICATION.TABLE}` (
  `id` INT PRIMARY KEY AUTO_INCREMENT,
  `userid` INT UNIQUE NOT NULL,
  `seriesid` INT NOT NULL,
  FOREIGN KEY (`userid`) REFERENCES `${USERS.TABLE}`(`id`),
  FOREIGN KEY (`seriesid`) REFERENCES `${SERIES.TABLE}`(`id`)
  );
